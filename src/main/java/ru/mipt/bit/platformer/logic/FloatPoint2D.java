package ru.mipt.bit.platformer.logic;

public class FloatPoint2D {
    private final float x;
    private final float y;

    public FloatPoint2D(final float x, final float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
